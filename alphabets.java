class alphabets
{
    public static void main(String[] args){
        char ch='A';
        for(int i=1;i<=26;i++)
        {
            System.out.print(ch+" ");
            ch++;
        }
        System.out.println();
        for(char c='a';c<='z';c++)
        {
            System.out.print(c+" ");
        }
    }
}